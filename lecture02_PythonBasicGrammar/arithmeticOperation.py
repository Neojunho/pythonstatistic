# 더하기 +
a = 5
b = 10
덧셈결과 = a + b
print('덧셈결과', 덧셈결과)

# 빼기 -
c = 1.5
d = 2.5
뺄셈결과 = d - c
print('뺄셈결과', 뺄셈결과)

# 곱하기 *
e = 5
f = 10
곱셈결과 = e * f
print('곱셈결과', 곱셈결과)

# 나누기 *
g = 15
h = 10
나눗셈결과 = g / h
print('나눗셈결과', 나눗셈결과)

# 나눗셈의 나머지 %
나머지 = g % h
print('나머지', 나머지)

# 나눗셈의 몫 //
몫 = g // h
print('몫', 몫)

# 제곱하기 **
i = 2
j = 10
제곱결과 = i ** j
print('제곱결과', 제곱결과)